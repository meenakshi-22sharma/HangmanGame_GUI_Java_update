public void input()
{   
    s=jTextField2.getText();
    ss=new char[s.length()];
    t=new JTextField[s.length()];
    ss=s.toCharArray();

         for(int i=0;i<s.length();i++)
              {
                   t[i]=new JTextField();
                    t[i].setMaximumSize(new Dimension(30,30));
                   if(ss[i]=='a'||ss[i]=='e'||ss[i]=='o'||ss[i]=='u'||ss[i]=='i') 
                   {
                       t[i].setText(String.valueOf(ss[i]));
                       total_check++;
                      
                   }
                    t[i].setEditable(false);
                     textFieldConstraints.fill = GridBagConstraints.NONE;
                        textFieldConstraints.weightx = 0.5;
                        textFieldConstraints.insets = new Insets(5,5,5,5);
                  p.add(t[i],textFieldConstraints);
                  p.updateUI();
                  
              }
         
}
