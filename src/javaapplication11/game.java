  public int check_validate()
  {
      boolean b;
       try
   {
    s=jTextField2.getText();
     
         Pattern p = Pattern.compile("[^a-z ]", Pattern.CASE_INSENSITIVE);
Matcher m = p.matcher(s);
 b = m.find();

if (b)
{ JOptionPane.showMessageDialog(null, "Please Enter String(a-z) only"); 
 return 0;
}

     
   }
catch(InputMismatchException e1)
{
    JOptionPane.showMessageDialog(null, "Please Enter String only"); return 0;
}
catch(Exception e)
{
    JOptionPane.showMessageDialog(null, "ERROR !! Enter again "); return 0;
}
    return 1;    
 }
